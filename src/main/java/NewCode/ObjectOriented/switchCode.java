public class switchCode {
    public static void main(String[] args) {
        String fruit = "apple";
        switch (fruit) {
            case "apple":
                System.out.println("apple");
                break;
            case "orange":
                System.out.println("orange");
                break;
            default:
                System.out.println("No fruit selected!");
        }
    }
}
